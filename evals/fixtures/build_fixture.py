"""Generate evals/fixtures/seed.sql — a small, deterministic CI fixture.

Exports every gold-set report plus a fixed sample of distractor reports (and
all their chunks + embeddings) from the live database into INSERT statements.
CI loads this into a throwaway pgvector container so retrieval evals run
hermetically: no NTSB download, no corpus embedding, deterministic rows.

Usage (against a populated local DB):
    poetry run python -m evals.fixtures.build_fixture
"""

from __future__ import annotations

import json
from pathlib import Path

import psycopg

from blackbox_qa import db

GOLD = Path(__file__).resolve().parents[1] / "gold" / "retrieval_gold.jsonl"
OUT = Path(__file__).resolve().parent / "seed.sql"
N_DISTRACTORS = 60  # plus the 12 gold reports -> a non-trivial but tiny corpus
EMB_DECIMALS = 6  # round embeddings to keep the committed fixture small (~no retrieval impact)

REPORT_COLS = (
    "ev_id, ntsb_no, ev_date, ev_year, ev_month, ev_type, ev_city, ev_state, "
    "ev_country, latitude, longitude, ev_highest_injury, inj_tot_fatal, "
    "inj_tot_serious, inj_tot_minor, inj_tot_none, inj_tot_total, wx_cond_basic, "
    "light_cond, acft_make, acft_model, acft_series, acft_category, far_part, damage"
)
CHUNK_COLS = "ev_id, chunk_index, source, content, embedding"


def _gold_ev_ids() -> list[str]:
    ids: list[str] = []
    for line in GOLD.read_text(encoding="utf-8").splitlines():
        line = line.strip()
        if line and not line.startswith("#"):
            ids.append(json.loads(line)["ev_id"])
    return ids


def main() -> int:
    gold = _gold_ev_ids()
    with db.connect() as conn:
        distractors = [
            r[0]
            for r in conn.execute(
                "SELECT ev_id FROM reports WHERE ev_id <> ALL(%s) ORDER BY ev_id LIMIT %s",
                (gold, N_DISTRACTORS),
            ).fetchall()
        ]
        ev_ids = sorted(set(gold) | set(distractors))

        report_rows = conn.execute(
            f"SELECT {REPORT_COLS} FROM reports WHERE ev_id = ANY(%s) ORDER BY ev_id",
            (ev_ids,),
        ).fetchall()
        chunk_rows = conn.execute(
            f"SELECT {CHUNK_COLS} FROM report_chunks WHERE ev_id = ANY(%s) "
            "ORDER BY ev_id, source, chunk_index",
            (ev_ids,),
        ).fetchall()
        # Round embeddings (last column) to shrink the committed SQL; the rerank
        # gate and baseline are measured against this same rounded fixture.
        chunk_rows = [
            (*row[:-1], [round(float(v), EMB_DECIMALS) for v in row[-1]]) for row in chunk_rows
        ]

        report_ph = "(" + ", ".join(["%s"] * len(REPORT_COLS.split(","))) + ")"
        chunk_ph = "(" + ", ".join(["%s"] * len(CHUNK_COLS.split(","))) + ")"
        # ClientCursor does client-side parameter binding, so mogrify() returns
        # a ready-to-write SQL string (with the registered pgvector adapter).
        cur = psycopg.ClientCursor(conn)
        report_inserts = [
            cur.mogrify(f"INSERT INTO reports ({REPORT_COLS}) VALUES {report_ph};", row)
            for row in report_rows
        ]
        chunk_inserts = [
            cur.mogrify(f"INSERT INTO report_chunks ({CHUNK_COLS}) VALUES {chunk_ph};", row)
            for row in chunk_rows
        ]

    header = (
        "-- GENERATED by evals/fixtures/build_fixture.py -- do not edit by hand.\n"
        f"-- {len(report_rows)} reports ({len(gold)} gold + {len(distractors)} distractors), "
        f"{len(chunk_rows)} chunks. Apply sql/schema.sql first.\n"
        "BEGIN;\n"
    )
    OUT.write_text(header + "\n".join(report_inserts) + "\n" + "\n".join(chunk_inserts) + "\nCOMMIT;\n")
    print(f"wrote {OUT}: {len(report_rows)} reports, {len(chunk_rows)} chunks")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
